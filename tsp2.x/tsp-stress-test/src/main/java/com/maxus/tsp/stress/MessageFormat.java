package com.maxus.tsp.stress;

public class MessageFormat {
	private String beginSign;
	private String msgSize;
	private String sn;
	private String encyptMode;
	private String command;
	private String seqNum;
	private String paramSize;
	private String param;
	private String checksum;

	public String getBeginSign() {
		return beginSign;
	}

	public void setBeginSign(String beginSign) {
		this.beginSign = beginSign;
	}

	public String getMsgSize() {
		return msgSize;
	}

	public void setMsgSize(String msgSize) {
		this.msgSize = msgSize;
	}

	public String getSn() {
		return sn;
	}

	public void setSn(String sn) {
		this.sn = sn;
	}

	public String getEncyptMode() {
		return encyptMode;
	}

	public void setEncyptMode(String encyptMode) {
		this.encyptMode = encyptMode;
	}

	public String getCommand() {
		return command;
	}

	public void setCommand(String command) {
		this.command = command;
	}

	public String getSeqNum() {
		return seqNum;
	}

	public void setSeqNum(String seqNum) {
		this.seqNum = seqNum;
	}

	public String getParamSize() {
		return paramSize;
	}

	public void setParamSize(String paramSize) {
		this.paramSize = paramSize;
	}

	public String getParam() {
		return param;
	}

	public void setParam(String param) {
		this.param = param;
	}

	public String getChecksum() {
		return checksum;
	}

	public void setChecksum(String checksum) {
		this.checksum = checksum;
	}

}
