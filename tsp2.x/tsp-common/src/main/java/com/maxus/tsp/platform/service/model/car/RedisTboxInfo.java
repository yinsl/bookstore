package com.maxus.tsp.platform.service.model.car;

public class RedisTboxInfo {

	private int id;
	private String tboxSn;
	private String value;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTboxSn() {
		return tboxSn;
	}
	public void setTboxSn(String tboxSn) {
		this.tboxSn = tboxSn;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	
}
